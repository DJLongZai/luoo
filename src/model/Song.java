package model;

public class Song {
	private String coverSrc;
	private String id;
	private String journalId;
	private String likeCnunt;
	private String MusicSrc;
	private String Singer;
	private String Song;
	private String musicStyle;
	public String getCoverSrc() {
		return coverSrc;
	}
	public void setCoverSrc(String coverSrc) {
		this.coverSrc = coverSrc;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getJournalId() {
		return journalId;
	}
	public void setJournalId(String journalId) {
		this.journalId = journalId;
	}
	public String getLikeCnunt() {
		return likeCnunt;
	}
	public void setLikeCnunt(String likeCnunt) {
		this.likeCnunt = likeCnunt;
	}
	public String getMusicSrc() {
		return MusicSrc;
	}
	public void setMusicSrc(String musicSrc) {
		MusicSrc = musicSrc;
	}
	public String getSinger() {
		return Singer;
	}
	public void setSinger(String singer) {
		Singer = singer;
	}
	public String getSong() {
		return Song;
	}
	public void setSong(String song) {
		Song = song;
	}
	public String getMusicStyle() {
		return musicStyle;
	}
	public void setMusicStyle(String musicStyle) {
		this.musicStyle = musicStyle;
	}
	
	
}
